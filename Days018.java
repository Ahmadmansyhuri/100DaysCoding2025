package isc100dayscooding;

public class Days018 {
    public static void main(String[] args) {
        byte D = 127;
        long E = D*10;
        
        System.out.println("=====(byte D = 127; & long E = D;)=====");
        System.out.println();
        System.out.println("NILAI BYTE = " + D);
        System.out.println("NILAI LONG = " + E);
        
        float  G = 45.1980f;
        double H = G;
        
        System.out.println("\n=====(float  G = 45.1980f; & double H = G;)=====");
        System.out.println();
        System.out.println("NILAI FLOAT  \t= " + G);
        System.out.println("NILAI DOUBLE \t= " + H);
    }
    
}
